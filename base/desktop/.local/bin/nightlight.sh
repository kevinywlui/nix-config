#!/usr/bin/env bash
# nightlight.sh — display colour-temperature manager for Sway/Wayland
#
# Owns ALL gamma/redness on the display.  Two layers, one writer:
#
#   1. Baseline curve — a gentle dawn/dusk colour-temperature ramp
#      (DAY_TEMP..NIGHT_TEMP by sun position) applied continuously by the
#      nightlight-curve.timer.  This is the "always on" night light.
#   2. Bedtime profile — an aggressive red shift (BEDTIME_KELVIN) layered on
#      top on a schedule, with manual on/off, pause, and skip-tonight controls.
#
# Both layers funnel through a single writer, wl-gammarelay-rs (D-Bus), so the
# two can never fight over the exclusive wlr-gamma-control lease.
#
# States (the bedtime profile's state machine):
#   off      — baseline curve only; the dawn/dusk ramp is in effect
#   on       — bedtime active; aggressive red applied, curve timer stopped
#   paused   — bedtime temporarily suspended; auto-resumes after PAUSE_MINUTES
#   disabled — bedtime skipped for tonight; re-enables at next bedtime
#
# The state file lives in XDG_RUNTIME_DIR (tmpfs, cleared on logout).
# Schedule config lives in ~/.config/nightlight/config.
#
# Usage: nightlight.sh {on|off|toggle|pause [min]|toggle-pause|disable|status|status-eww|--reapply|--gamma-update|--schedule-activate|--schedule-deactivate}

set -euo pipefail

# ---------------------------------------------------------------------------
# Config
#
# The config file is the single source of truth for the schedule and is
# generated by Nix (nix/modules/home/services/nightlight/default.nix) so the systemd
# timer OnCalendar and these values can never drift apart.  The assignments
# below are only fallbacks for non-NixOS installs where the file is absent.
# ---------------------------------------------------------------------------
BEDTIME_HOUR=21     # 0-23; bedtime activation hour (timer fire is OnCalendar)
WAKETIME_HOUR=7     # 0-23; deactivation hour — also bounds the activate window
PAUSE_MINUTES=30    # default pause length; overridable per-call (see cmd_pause)
FADE_SECONDS=5      # gamma fade for manual toggles
WINDDOWN_SECONDS=60 # gentle gamma fade for the scheduled 9pm activation

CONFIG_FILE="${XDG_CONFIG_HOME:-$HOME/.config}/nightlight/config"
if [[ -f "$CONFIG_FILE" ]]; then
	# shellcheck source=/dev/null
	source "$CONFIG_FILE"
fi

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------
# 1400K is a deep amber: it suppresses nearly all blue and most green, kept
# just above the near-monochrome orange of ~1000K so the bar/menu stay legible
# at night.  Bedtime warms the colour only — brightness is always left at full
# (1.0), the same as the daytime curve, so the screen never dims.
BEDTIME_KELVIN=1400

# Baseline-curve endpoints for the dawn/dusk calculation (see _calc_temp).
DAY_TEMP=5700
NIGHT_TEMP=2000
LOCATION=(-l 37.32:-122.03)

# wl-gammarelay-rs D-Bus address.  Bus name has a hyphen (rs.wl-gammarelay);
# the interface name does not (rs.wl.gammarelay) — interface names cannot
# contain hyphens.
DBUS_DEST=rs.wl-gammarelay
DBUS_IFACE=rs.wl.gammarelay

STATE_FILE="${XDG_RUNTIME_DIR:-/run/user/$(id -u)}/nightlight.state"
RAMP_TOKEN_FILE="${XDG_RUNTIME_DIR:-/run/user/$(id -u)}/nightlight.ramp"

# Transient systemd unit names for the pause countdown
WARN_UNIT="nightlight-warn"
RESUME_UNIT="nightlight-resume"

# ---------------------------------------------------------------------------
# State file I/O
#
# File format (one field per line):
#   line 1: state         (on | off | paused | disabled)
#   line 2: pause_until   (epoch; present only when state=paused)
#   line 2: disabled_until (epoch; present only when state=disabled)
# ---------------------------------------------------------------------------

_read_line() {
	local n="$1"
	if [[ -f "$STATE_FILE" ]]; then
		sed -n "${n}p" "$STATE_FILE"
	fi
}

read_state() {
	local s
	s="$(_read_line 1)"
	printf '%s' "${s:-off}"
}

read_epoch2() {
	# Second line — pause_until or disabled_until depending on state
	_read_line 2
}

write_state() {
	local state="$1"
	local epoch2="${2:-}"
	# Atomic write: write a temp file on the same tmpfs, then rename(2).  A
	# concurrent reader (eww poll, curve tick) then sees either the whole old
	# file or the whole new one — never a truncated line caught mid-write.
	local tmp="${STATE_FILE}.$$"
	if [[ -n "$epoch2" ]]; then
		printf '%s\n%s\n' "$state" "$epoch2" >"$tmp"
	else
		printf '%s\n' "$state" >"$tmp"
	fi
	mv -f "$tmp" "$STATE_FILE"
}

# ---------------------------------------------------------------------------
# Notifications — advisory only, never fatal.  A missing notification daemon
# must not abort (set -e) a state change that has already been applied.
# ---------------------------------------------------------------------------

notify() {
	notify-send "$@" 2>/dev/null || true
}

# ---------------------------------------------------------------------------
# Gamma helpers — wl-gammarelay-rs via D-Bus
# ---------------------------------------------------------------------------

_relay_get() {
	# $1 = property name (Temperature|Brightness); prints the bare value.
	# Trailing `|| true`: under `set -o pipefail` a down relay would make the
	# pipeline non-zero and abort the (set -e) caller mid-command.  An absent
	# value is not fatal here — callers already default it.
	busctl --user get-property "$DBUS_DEST" / "$DBUS_IFACE" "$1" 2>/dev/null |
		awk '{print $2}' || true
}

_relay_apply() {
	# Set temperature+brightness immediately (no fade).
	local temp="$1" brightness="$2"
	busctl --user set-property "$DBUS_DEST" / "$DBUS_IFACE" Temperature q "$temp" 2>/dev/null || true
	busctl --user set-property "$DBUS_DEST" / "$DBUS_IFACE" Brightness d "$brightness" 2>/dev/null || true
}

# Fade temperature from the current value to the target over the given duration,
# setting brightness once at the start.  The token file gives last-writer-wins:
# a newer transition claims the token and any older fade bails on its next step,
# so a quick re-click can't leave two fades fighting.
_relay_fade() {
	local target_temp="$1" target_bri="$2" duration="${3:-$FADE_SECONDS}"
	local token=$$
	echo "$token" >"$RAMP_TOKEN_FILE"

	local start_temp steps=$((duration * 4)) i temp
	start_temp="$(_relay_get Temperature)"
	start_temp="${start_temp:-$target_temp}"
	((steps < 1)) && steps=1

	busctl --user set-property "$DBUS_DEST" / "$DBUS_IFACE" Brightness d "$target_bri" 2>/dev/null || true
	for ((i = 1; i <= steps; i++)); do
		[[ "$(cat "$RAMP_TOKEN_FILE" 2>/dev/null)" == "$token" ]] || return 0
		temp=$((start_temp + (target_temp - start_temp) * i / steps))
		busctl --user set-property "$DBUS_DEST" / "$DBUS_IFACE" Temperature q "$temp" 2>/dev/null || true
		sleep 0.25
	done
}

# Current baseline-curve temperature for the dawn/dusk ramp, computed without a
# Wayland connection.  gammastep is used purely as a calculator here (dummy
# mode, no display output); it prints "Notice: Color temperature: NNNNK" to
# stderr, which we parse.  LC_ALL=C pins the message wording so the parse can't
# break under a non-English locale; `|| true` ensures a parse miss yields empty
# output (handled by callers) rather than a pipefail abort of the set -e caller.
_calc_temp() {
	LC_ALL=C gammastep -m dummy -p "${LOCATION[@]}" -t "${DAY_TEMP}:${NIGHT_TEMP}" 2>&1 |
		grep -m1 -oP '(?<=Color temperature: )\d+' || true
}

apply_bedtime_ramp() {
	# $1 (optional) = fade duration in seconds (defaults to FADE_SECONDS).
	# Stop the baseline-curve timer so it cannot overwrite bedtime values,
	# then fade down to the deep-red bedtime values.
	local duration="${1:-$FADE_SECONDS}"
	systemctl --user stop nightlight-curve.timer 2>/dev/null || true
	systemctl --user stop nightlight-curve.service 2>/dev/null || true
	_relay_fade "$BEDTIME_KELVIN" 1.0 "$duration"
}

restore_curve() {
	# Cancel any in-flight fade first (the new fade runs in the timer-launched
	# service a moment later, so without this an on→off could leave one stray
	# step from the old fade), then restart the timer: OnActiveSec=0s fires
	# --gamma-update immediately, which fades back up to the baseline-curve value.
	: >"$RAMP_TOKEN_FILE"
	systemctl --user restart nightlight-curve.timer
}

# ---------------------------------------------------------------------------
# Pause timer helpers (transient systemd-run units)
# ---------------------------------------------------------------------------

cancel_pause_timers() {
	systemctl --user stop "${WARN_UNIT}.timer" 2>/dev/null || true
	systemctl --user stop "${WARN_UNIT}.service" 2>/dev/null || true
	systemctl --user stop "${RESUME_UNIT}.timer" 2>/dev/null || true
	systemctl --user stop "${RESUME_UNIT}.service" 2>/dev/null || true
}

start_pause_timers() {
	local warn_delay=$(((PAUSE_MINUTES - 2) * 60))
	local resume_delay=$((PAUSE_MINUTES * 60))
	# A pause shorter than the 2-minute warning lead would make warn_delay <= 0,
	# which systemd-run rejects — aborting before the resume timer is even armed
	# (so the pause would never auto-resume).  Clamp to fire the warning at once.
	((warn_delay < 1)) && warn_delay=1

	# Transient `systemd-run --user` units start with an empty PATH (unlike the
	# Nix-declared units in default.nix, which set Environment=PATH=…).  An empty
	# PATH meant notify-send/bash/nightlight.sh's `#!/usr/bin/env bash` couldn't
	# resolve, so the resume unit silently died with exit 127 and pause never
	# auto-resumed (the bar wedged at "PAUSED 0m").  Mirror serviceEnv verbatim.
	# `%h` is a unit-file specifier and does NOT expand in --setenv argv — use
	# $HOME so the shell substitutes before systemd-run is invoked.
	local pathenv="PATH=/run/current-system/sw/bin:$HOME/.local/bin"

	# Invoke through the $HOME symlink rather than realpath: home-manager's
	# mkOutOfStoreSymlink resolves to a /nix/store path that changes each
	# generation, so a pause that spans an `nh os switch` + GC could point
	# the unit at a deleted store path.  The symlink is updated atomically.
	local self="$HOME/.local/bin/nightlight.sh"

	# urgency=normal, not critical: critical bypasses do-not-disturb and never
	# auto-expires on most daemons — wrong for a "screen is about to warm up" cue.
	systemd-run --user \
		--unit="$WARN_UNIT" \
		--on-active="${warn_delay}s" \
		--timer-property=AccuracySec=1s \
		--setenv="$pathenv" \
		notify-send \
		-u normal \
		-t 120000 \
		-h string:x-dunst-stack-tag:bedtime-warning \
		"Bedtime resuming in 2 min" \
		"Click pause again to extend 30m"

	# No `bash -c` wrapper: the previous `dunstctl close-all` prelude was
	# cosmetic — the warn notification has -t 120000 (2-min self-expire) and a
	# stack tag dunst replaces on the next push, so a stale toast can't pile up.
	systemd-run --user \
		--unit="$RESUME_UNIT" \
		--on-active="${resume_delay}s" \
		--timer-property=AccuracySec=1s \
		--setenv="$pathenv" \
		"$self" on
}

# ---------------------------------------------------------------------------
# Schedule helpers
# ---------------------------------------------------------------------------

# Epoch for "tomorrow at HOUR:00:00" — used by cmd_disable (see why there).
epoch_for_hour_tomorrow() {
	local hour="$1"
	date -d "tomorrow ${hour}:00:00" +%s
}

# Is the given hour (0-23) inside the bedtime window [BEDTIME_HOUR, WAKETIME_HOUR)?
# Handles the usual midnight wrap (e.g. 21:00..07:00).  Called only in `if`
# conditions, so its (( )) exit status is safe under set -e.
_in_bedtime_window() {
	local h="$1"
	if ((BEDTIME_HOUR < WAKETIME_HOUR)); then
		((h >= BEDTIME_HOUR && h < WAKETIME_HOUR))
	else
		((h >= BEDTIME_HOUR || h < WAKETIME_HOUR))
	fi
}

# Check if a DISABLED state has expired (disabled_until <= now)
maybe_clear_disabled() {
	local state
	state="$(read_state)"
	if [[ "$state" == "disabled" ]]; then
		local disabled_until now
		disabled_until="$(read_epoch2)"
		now=$(date +%s)
		if [[ -n "$disabled_until" && "$now" -ge "$disabled_until" ]]; then
			write_state "off"
		fi
	fi
}

# Catch-up if the transient resume timer never fired (PATH bug, manual stop,
# any future failure).  Mirrors the paused branch of cmd_reapply but runs from
# the 1s eww poll, so the bar can't get wedged at "PAUSED 0m" even when the
# laptop never suspends (cmd_reapply is only triggered by swayidle after-resume).
# Cheap on purpose — single busctl call, no fade, no notify — so a poll caller
# never blocks.  The curve timer's next tick is guarded by state=="on" in
# cmd_gamma_update, so we don't need to stop it here.
maybe_complete_pause() {
	[[ "$(read_state)" == "paused" ]] || return 0
	local pause_until now
	pause_until="$(read_epoch2)"
	now=$(date +%s)
	[[ -n "$pause_until" && "$now" -ge "$pause_until" ]] || return 0
	cancel_pause_timers
	if _in_bedtime_window "$(date +%-H)"; then
		write_state "on"
		_relay_apply "$BEDTIME_KELVIN" 1.0
	else
		write_state "off"
		restore_curve
	fi
}

# ---------------------------------------------------------------------------
# Commands
# ---------------------------------------------------------------------------

cmd_on() {
	# $1 (optional) = fade duration (scheduled activation passes the wind-down).
	# State is written before the gamma change so the curve timer's guard
	# (skip when state=on) sees the new state and can't race-override the ramp.
	local duration="${1:-$FADE_SECONDS}"
	cancel_pause_timers
	write_state "on"
	apply_bedtime_ramp "$duration"
	notify -u low -t 4000 \
		"Bedtime mode on" \
		"Warm ${BEDTIME_KELVIN}K
Left-click bar to pause · Right-click to turn off"
}

cmd_off() {
	# Called by: user toggle while on/paused, --schedule-deactivate
	cancel_pause_timers
	write_state "off"
	restore_curve
	notify -u low -t 4000 \
		"Bedtime mode off" \
		"Gamma restored"
}

cmd_pause() {
	# $1 (optional) = pause length in minutes, overriding the config default.
	# If already paused: extend (reset) the timer.  If on: pause.  Else no-op.
	PAUSE_MINUTES="${1:-$PAUSE_MINUTES}"
	local state
	state="$(read_state)"

	case "$state" in
	off | disabled)
		return 0
		;;
	on | paused)
		local pause_until end_time
		pause_until=$(($(date +%s) + PAUSE_MINUTES * 60))
		end_time=$(date -d "@${pause_until}" +%H:%M)
		cancel_pause_timers
		write_state "paused" "$pause_until"
		restore_curve
		start_pause_timers
		if [[ "$state" == "paused" ]]; then
			notify -u low -t 4000 \
				"Pause extended" \
				"Resuming at ${end_time}"
		else
			notify -u low -t 4000 \
				"Bedtime paused ${PAUSE_MINUTES} minutes" \
				"Resuming at ${end_time} · Left-click bar to resume now"
		fi
		;;
	esac
}

cmd_disable() {
	# Skip tonight's bedtime; re-enables at tomorrow's bedtime.
	# Called by: $mod+Shift+d, or by hand.
	#
	# disabled_until targets *tomorrow's* bedtime, not today's: if it were
	# today's, a skip set before bedtime would be cleared by the
	# maybe_clear_disabled check that --schedule-activate runs at exactly that
	# hour (now >= disabled_until), and bedtime would activate anyway.  The 7am
	# --schedule-deactivate clears the disabled state regardless, so targeting
	# tomorrow only needs to outlast tonight's activation check.
	cancel_pause_timers

	local disabled_until
	disabled_until="$(epoch_for_hour_tomorrow "$BEDTIME_HOUR")"
	local resume_time
	resume_time=$(date -d "@${disabled_until}" "+%a %-I:%M %p")

	write_state "disabled" "$disabled_until"
	restore_curve
	notify -u low -t 5000 \
		"Bedtime skipped tonight" \
		"Will re-enable ${resume_time}"
}

cmd_toggle() {
	# $mod+n escape hatch: flip bedtime on/off regardless of schedule.
	#   on/paused → off; off/disabled → on (override a tonight-skip).
	local state
	state="$(read_state)"
	case "$state" in
	on | paused) cmd_off ;;
	off | disabled) cmd_on ;;
	esac
}

cmd_toggle_pause() {
	# Right-click from Eww: on→pause, paused→resume-now, off/disabled→no-op
	local state
	state="$(read_state)"
	case "$state" in
	on)
		cmd_pause
		;;
	paused)
		cancel_pause_timers
		write_state "on"
		apply_bedtime_ramp
		notify -u low -t 4000 "Bedtime mode resumed" ""
		;;
	off | disabled)
		: # no-op
		;;
	esac
}

cmd_reapply() {
	# Called by swayidle after-resume.  Re-push values immediately (no fade —
	# a fade after a DPMS wake would look like a flash).
	local state
	state="$(read_state)"
	case "$state" in
	on)
		_relay_apply "$BEDTIME_KELVIN" 1.0
		;;
	paused)
		# A long suspend can outlast the resume timer (a monotonic, non-
		# persistent transient unit).  If the pause window already elapsed by
		# wake, complete the resume the dead timer owed us — otherwise the state
		# stays "paused" with a dead timer and the pill shows "PAUSED 0m" forever.
		# But only re-enter bedtime if we woke still inside the night window: a
		# pause that elapses past the wake hour must settle to the curve, not
		# redden the screen in the morning (mirrors the cmd_schedule_activate
		# guard, and converges with the 7am deactivate catch-up either way).
		local pause_until now
		pause_until="$(read_epoch2)"
		now=$(date +%s)
		if [[ -n "$pause_until" && "$now" -ge "$pause_until" ]]; then
			if _in_bedtime_window "$(date +%-H)"; then
				cmd_on
			else
				cmd_off
			fi
		else
			local temp
			temp="$(_calc_temp)"
			if [[ -n "$temp" ]]; then
				_relay_apply "$temp" 1.0
			fi
		fi
		;;
	off)
		local temp
		temp="$(_calc_temp)"
		if [[ -n "$temp" ]]; then
			_relay_apply "$temp" 1.0
		fi
		;;
	esac
}

cmd_gamma_update() {
	# Fired by nightlight-curve.timer every few minutes, and immediately on
	# restore (bedtime off/pause).  Pushes the baseline-curve dawn/dusk
	# temperature to the relay.  Skips while bedtime is on so a stray fire
	# can't override it.
	[[ "$(read_state)" == "on" ]] && return 0
	local temp cur delta
	temp="$(_calc_temp)"
	# No value to apply (e.g. gammastep not yet resolvable at early session) is
	# a skip, not a failure — return 0 so the oneshot isn't marked failed.
	[[ -z "$temp" ]] && return 0
	cur="$(_relay_get Temperature)"
	cur="${cur:-$temp}"
	delta=$((temp > cur ? temp - cur : cur - temp))
	# Fade large jumps (restoring from bedtime); apply tiny dusk steps directly.
	if ((delta > 300)); then
		_relay_fade "$temp" 1.0
	else
		_relay_apply "$temp" 1.0
	fi
}

# --schedule-activate: fired by nightlight-bedtime-on.service (9pm timer)
cmd_schedule_activate() {
	maybe_clear_disabled
	# A Persistent timer fires a *missed* activation on the next session start.
	# If the laptop was suspended/off across 9pm and opened in the morning, that
	# catch-up would otherwise redden the screen at, say, 8am.  Only activate if
	# we are actually inside the night window now.
	local now_h
	now_h=$(date +%-H)
	if ! _in_bedtime_window "$now_h"; then
		return 0
	fi
	local state
	state="$(read_state)"
	case "$state" in
	disabled)
		# User disabled for tonight — honour it, don't activate
		notify -u low -t 4000 \
			"Bedtime mode" \
			"Scheduled activation skipped (disabled tonight)"
		;;
	on | paused)
		# Already active (e.g. user turned it on manually before 9pm)
		: # no-op
		;;
	off)
		# Gentle wind-down rather than the quick manual fade.
		cmd_on "$WINDDOWN_SECONDS"
		;;
	esac
}

# --schedule-deactivate: fired by nightlight-bedtime-off.service (7am timer)
cmd_schedule_deactivate() {
	maybe_clear_disabled
	local state
	state="$(read_state)"
	case "$state" in
	on | paused)
		cmd_off
		;;
	disabled)
		# Clear disabled state at wake time — tonight's window is over
		cancel_pause_timers
		write_state "off"
		restore_curve
		;;
	off)
		: # already off, nothing to do
		;;
	esac
}

cmd_status() {
	maybe_clear_disabled
	read_state
}

cmd_status_eww() {
	maybe_clear_disabled
	maybe_complete_pause
	local state pause_until text css_class
	state="$(read_state)"

	case "$state" in
	off)
		text="DAY"
		css_class="nightlight-off"
		;;
	on)
		text="NIGHT"
		css_class="nightlight-on"
		;;
	paused)
		pause_until="$(read_epoch2)"
		if [[ -n "$pause_until" ]]; then
			local now remaining_min
			now=$(date +%s)
			remaining_min=$(((pause_until - now + 59) / 60))
			[[ "$remaining_min" -lt 0 ]] && remaining_min=0
			# Always show countdown — "PAUSED 28m" so the user always knows
			# how long remains.  Amber color already signals NIGHT context.
			text="PAUSED ${remaining_min}m"
		else
			text="PAUSED"
		fi
		css_class="nightlight-paused"
		;;
	disabled)
		# "OFF TONIGHT": clear meaning, short, not alarming like "DISABLED".
		# Muted-yellow color signals deliberate tonight-only skip.
		text="OFF TONIGHT"
		css_class="nightlight-disabled"
		;;
	esac

	printf '{"state":"%s","text":"%s","class":"%s"}\n' \
		"$state" "$text" "$css_class"
}

# ---------------------------------------------------------------------------
# Dispatch
# ---------------------------------------------------------------------------

CMD="${1:-}"
case "$CMD" in
on) cmd_on ;;
off) cmd_off ;;
toggle) cmd_toggle ;;
pause) cmd_pause "${2:-}" ;;
disable) cmd_disable ;;
toggle-pause) cmd_toggle_pause ;;
--reapply) cmd_reapply ;;
--gamma-update) cmd_gamma_update ;;
--schedule-activate) cmd_schedule_activate ;;
--schedule-deactivate) cmd_schedule_deactivate ;;
status) cmd_status ;;
status-eww) cmd_status_eww ;;
*)
	printf 'Usage: %s {on|off|toggle|pause [min]|disable|toggle-pause|--reapply|--gamma-update|--schedule-activate|--schedule-deactivate|status|status-eww}\n' \
		"$(basename "$0")" >&2
	exit 1
	;;
esac
